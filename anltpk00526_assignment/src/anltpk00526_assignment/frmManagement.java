    private boolean KiemTraEmail(String ktEmail){
        boolean ketQua = false;
        String EMAIL_PATTERN = 
		"^[_A-Za-z0-9-\\+]+(\\.[_A-Za-z0-9-]+)*@"
		+ "[A-Za-z0-9-]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$";
        Pattern p = Pattern.compile(EMAIL_PATTERN);
        Matcher m = p.matcher(ktEmail);
        
        ketQua = m.find();
        
        return ketQua;
    }
    private boolean KiemTraSo(String ktSo){
        boolean ketQua = false;
        
        Pattern p = Pattern.compile("^[0-9]");
        Matcher m = p.matcher(ktSo);
        
        ketQua = m.find();
        
        return ketQua;
    }
    
