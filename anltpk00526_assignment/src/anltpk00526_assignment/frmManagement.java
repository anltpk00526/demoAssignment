    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String name, age, address, email, salary;
        name = txtName.getText();
        age = txtAge.getText();
        address = txtAddress.getText();
        email = txtEmail.getText();
        salary = txtSalary.getText();

        String cauTruyVan = "insert into NhanVien(Ten,Tuoi,DiaChi,Email,Luong) values(N'" + name + "'," + age + ",N'" + address + "',N'" + email + "'," + salary + ")";
        
        if (!name.equals("") && !age.equals("") && !address.equals("") && !email.equals("") && !salary.equals("")) {
            
            if(name.length()==0){
                frmLogin.ThongBao("Tên không được bỏ trống", "Thông báo", 2);
            }else if(KiemTraSo(age)==false){
                frmLogin.ThongBao("Tuổi nhập số", "Thông báo", 2);
            }else if(address.length()==0){
                frmLogin.ThongBao("Địa chỉ không được bỏ trống", "Thông báo", 2);
            }else if(KiemTraEmail(email)==false){
                frmLogin.ThongBao("Email không hợp lệ", "Thông báo", 2);
            }else if(KiemTraSo(salary)==false){
                frmLogin.ThongBao("Lương nhập số", "Thông báo", 2);
            }else{
                Anltpk00526_assignment.connection.ExcuteQueryUpdateDB(cauTruyVan);
            }
            
        } else {
            frmLogin.ThongBao("Nhập dữ liệu", "Thông báo", 2);
        }
        LayDuLieu();

    }//GEN-LAST:event_btnAddActionPerformed

