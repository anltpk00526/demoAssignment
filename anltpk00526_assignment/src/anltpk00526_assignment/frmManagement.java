    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        // TODO add your handling code here:
        String TuKhoaTimKiem = txtSearch.getText();
        String cauTruyVan = "select *from NhanVien where Ten like N'%" + TuKhoaTimKiem + "%'" + "or Email like N'%" + TuKhoaTimKiem + "%'";

        ResultSet rs = Anltpk00526_assignment.connection.ExcuteQueryGetTable(cauTruyVan);
        Object[] obj = new Object[]{"STT", "EMPLOYEE ID", "Name", "Age", "Address", "Email", "Salary"};
        DefaultTableModel tableModel = new DefaultTableModel(obj, 0);
        tblNhanVien.setModel(tableModel);
        try {
            while (rs.next()) {
                Object[] item = new Object[7];
                item[0] = tblNhanVien.getRowCount() + 1;
                item[1] = rs.getInt("NhanVienID");
                item[2] = rs.getString("Ten");
                item[3] = rs.getString("Tuoi");
                item[4] = rs.getString("DiaChi");
                item[5] = rs.getString("Email");
                item[6] = rs.getString("Luong");
                tableModel.addRow(item);
            }
        } catch (SQLException ex) {
            System.out.println(ex.toString());
        }
    }//GEN-LAST:event_txtSearchKeyReleased

